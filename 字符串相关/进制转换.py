#使用Python内置函数：bin()、int()、hex()可实现进制转换

#十进制转换为二进制
print("2进制到16进制",bin(171))

#2进制到16进制
print("2进制到16进制",hex(0b10101011))

#16进制到2进制
print("16进制到2进制",bin(0xab))

#2进制字符串转为10进制
print("2进制字符串转为10进制",int("10101011",2))


#10进制转为16进制
print("10进制转为16进制",hex(171))
